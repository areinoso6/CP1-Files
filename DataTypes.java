
public class DataTypes{
	public static void main(String[] args){
		System.out.println("Here are some data types:"); //String
		System.out.println("20 + 11"); //String
		System.out.println(20 + 11); //Integers
		System.out.println(17.4 - 3.2); //Decimal
		System.out.println("20" + "4"); //Integers
		System.out.println(20 + 4); //Integers
	}
}
